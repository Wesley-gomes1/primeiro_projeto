# Calculadora python
num1 = int(input("digite o primeiro numero: "))
num2 = int(input("digite o segundo numero: "))
soma = num1 + num2
subtracacao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2
divisao_inteira = num1 //num2
resto_divisao = num1 % num2
potenciacao = num1  ** num2

print(f"A soma de {num1} e {num2} é igual a = {soma}")

print(f"A subtracao de {num1} e {num2} é igual a = {subtracacao}")

print(f"A multiplicacao de {num1} e {num2} é igual a = {multiplicacao}")

print(f"A divisao de {num1} e {num2} é igual a = {divisao}")

print(f"A divisao inteira de {num1} e {num2} é igual a = {divisao_inteira}")

print(f"O resto da divisao {num1} e {num2} é igual a = {resto_divisao}")

print(f"A pontenciacao de {num1} e {num2} é igual a = {potenciacao}")
